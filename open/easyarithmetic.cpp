constexpr unsigned long long MODULO = 998244353;
using modint = MontgomeryModInt<integral_constant<decay<decltype(MODULO)>::type, MODULO>>;

struct SegmentTree {
    static vector<modint> p10;

    struct Segment {
        modint sum, l, r;
        int p, sgn;

        Segment() : sum(0), l(0), r(0), p(0), sgn(0) {}

        auto & operator=(const char &c) {
            sum = l = r = p = sgn = 0;
            if (isdigit(c)) {
                l = c - '0';
                p = 1;
            } else sgn = c == '+' ? 1 : -1;
            return *this;
        }

        friend auto operator+(const Segment &sl, const Segment &sr) {
            Segment seg;
            if (!sl.sgn) {
                seg = sr;
                seg.p += sl.p;
                seg.l = sr.l + p10[sr.p] * sl.l;
            } else if (!sr.sgn) {
                seg = sl;
                seg.r = sr.l + p10[sr.p] * sl.r;
            } else {
                seg.l = sl.l;
                seg.r = sr.r;
                seg.p = sl.p;
                seg.sgn = sr.sgn;
                seg.sum = sl.sum + sr.sum + sl.sgn * (sr.l + p10[sr.p] * sl.r);
            }
            return seg;
        }
    };

    string str;
    int n;
    vector<Segment> ST;

    void pull(int i) {
        ST[i] = ST[i << 1] + ST[i << 1 | 1];
    }

    void build() {
        for (int i = n - 1; i; i--) pull(i);
    }

    int midpoint(int l, int r) {
        int i = 1 << __lg(r - l);
        return min(l + i, r - (i >> 1));
    }

    void modify(const int &pos) {
        modify(1, pos, 0, n);
    }

    void modify(int i, const int &pos, int l, int r) {
        if (l + 1 == r) {
            ST[i] = str[l];
            return;
        }

        int m = midpoint(l, r);
        if (pos < m) modify(i << 1, pos, l, m);
        else modify(i << 1 | 1, pos, m, r);

        pull(i);
    }

    Segment range_query(int l, int r) {
        return range_query(1, l, r, 0, n);
    }

    Segment range_query(int i, int ql, int qr, int l, int r) {
        if (qr <= l || r <= ql) return {};
        if (ql <= l && r <= qr) return ST[i];

        int m = midpoint(l, r);
        return range_query(i << 1, ql, qr, l, m) + range_query(i << 1 | 1, ql, qr, m, r);
    }

    auto & operator[](int i) {
        return ST[i];
    }

    SegmentTree(int n, string str) : n(n), ST(2 * n), str(str) {
        p10.resize(n + 1, 1);
        for (int i = 0; i < n; i++) p10[i + 1] = p10[i] * 10;

        int m = bit_ceil((unsigned) n);
        for (int i = 0; i < str.size(); i++) ST[(i + m) % n + n] = str[i];
        build();
    }
};

vector<modint> SegmentTree::p10;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    modint::init();

    string s;
    int q;
    cin >> s >> q;

    int n = s.size();
    SegmentTree st(n, s);
    while (q--) {
        char ch;
        cin >> ch;

        if (ch == '?') {
            int l, r;
            cin >> l >> r;

            auto seg = st.range_query(l - 1, r);
            modint value = seg.sum + seg.l + seg.sgn * seg.r;
            cout << value << "\n";
        } else {
            int i;
            char c;
            cin >> i >> c;

            st.str[i - 1] = c;
            st.modify(i - 1);
        }
    }
}
